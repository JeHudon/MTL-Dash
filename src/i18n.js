export function useTranslation(locale) {
    const t = translations[locale] ?? translations["fr"];

    return (key, vars = {}) => {
        let str = t[key] ?? key;
        Object.entries(vars).forEach(([k, v]) => {
            str = str.replace(`{{${k}}}`, v);
        });
        return str;
    };
}
